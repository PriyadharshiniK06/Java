public class matrix_2d
{
    public void main(String[]args)
    {
        int a[][]={ {1,4,6,8},{2,3,6,8},{1,5,6,7}};
        int i,j;
        for(i=0;i<4;i++)
        {
            for(j=0;j<3;j++)
            {
                System.out.println("a"+"["+i+"]"+"["+j+"]" + a[i][j]);
            }
    
        }
        
    }
}            
    
